<div class="entry-content pagina">
    <p>Com o crescimento acelerado da utilização da internet e a obrigatoriedade da divulgação das informações de
        forma online, acessibilidade às informações eletrônicas também se tornou Lei, regulamentada pelo <a
            href="http://www.planalto.gov.br/ccivil_03/_ato2011-2014/2012/decreto/d7724.htm">decreto nº 7.724, de 16
            de maio de 2012</a>, que dispõe sobre o acesso a informações previsto no inciso XXXIII
        do&nbsp;caput&nbsp;do art. 5º, no inciso II do § 3º&nbsp;do art. 37 e no § 2º&nbsp;do art. 216 da
        Constituição.</p>

    <p>Para adequar-se à legislação e proporcionar acessibilidade na divulgação das informações, propiciando
        facilidades na localização de informações a todas as pessoas, este Portal foi desenvolvido com recursos
        necessários para assegurar o acesso às informações a todos os cidadãos.</p>

    <p>
        O Portal dispõe da opção de alto contraste, disponível em todas as páginas do Portal, e de quatro opções, que facilitam a navegação
        pelo conteúdo do Portal: "<u>[1] Ir para o conteúdo</u>", "<u>[2] Ir para o menu</u>", "<u>[3] Ir para a
            busca</u>" e "<u>[4] Ir para o rodapé</u>".</p>

    <p>Além de acionar os comandos por meio dos botões disponíveis no painel do site, é possível também utilizar as
        ferramentas por meio de atalhos, proporcionando ainda mais facilidade no acesso às informações. Confira
        abaixo os atalhos que são padrões do Governo Federal e que estão disponíveis neste Portal:</p>

    <p><strong>Nos navegadores Google Chrome e Internet Explorer/Edge use as seguintes teclas de atalho:</strong>
    </p>

    <p>Alt + 1 – Será direcionado para o início do conteúdo da página acessada.<br>
        Alt + 2 – O usuário é direcionado para o início do menu.<br>
        Alt + 3 – Posiciona o usuário diretamente ao campo de Busca Avançada do site.<br>
        Alt + 4 – Direcionado para as informações disponíveis no rodapé do site.</p>

    <p><strong>Caso o navegador utilizado para acessar o Portal seja um dos listados abaixo, confira as respectivas
            teclas de atalho para utilizar as funcionalidades:&nbsp;</strong></p>

    <p>Firefox: Tecle simultaneamente Alt + Shift + Número.<br>
        Firefox no Mac OS: Tecle simultaneamente Ctrl + Alt + Número.<br>
        Opera: Shift + Escape +Número ou Shift + Escape para abrir uma janela com todas as alternativas de ACCESSKEY
        da página.</p>
